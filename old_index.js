function createAlbum() {
	function shuffle(array) {
		var m = array.length,
			t,
			i;
		while (m) {
			i = Math.floor(Math.random() * m--);
			t = array[m];
			array[m] = array[i];
			array[i] = t;
		}
		return array[1];
	}

	let suggestion = shuffle(AKFG);
	return document.body.appendChild(genAlbum(suggestion));
}

